function admin_sanebullquote() {
	if ( isset($_POST['submitted']) ) {
		$options = array();

		$stocksymbols = $_POST['quotemappings'];
		if ( !empty($stocksymbols) ) {
			$replacement_array = array();
			foreach (explode("\n", $stocksymbols) AS $line) {
				list($shortcut, $text) = array_map('trim', explode("=>", $line, 2));
				if (!empty($shortcut)) $replacement_array[str_replace('\\', '', $shortcut)] = str_replace('\\', '', strtoupper($text));
			}
			$options['sanebullquotes'] = $replacement_array;
		}
		update_option('sbq', $options);
		echo '<div class="updated"><p>Plugin settings saved.</p></div>';
	}

	$options = get_option('sbq');
	$stocksymbols = $options['sanebullquotes'];
	$replacements = '';
	foreach ($stocksymbols AS $shortcut => $replacement) {
		$replacements .= "$shortcut => $replacement\n";
	}
	$action_url = $_SERVER[PHP_SELF] . '?page=sanebull/' . basename(__FILE__);
echo <<<END
	<div class='wrap'>\n
		<h2>SaneBull Quote Options</h2>\n
		<p>SaneBull Quote is a plugin that creates a tooltip with a stock quote and chart for a configured list of companies.
       No special tags are required within posts to achieve this - simply enter a company name => stock symbol mapping on this
       page, and the appropriate code will be generated automatically based on sanebull.com feed.
		</p>
		
<form name="dynamictextreplace" action="$action_url" method="post">
		<input type="hidden" name="submitted" value="1" />

		<p>In the textbox below, enter the companies that you would like to generate automatic stock quote tooltips for in your blog.</p>
    <p>The format should be the same as the examples in the box (Company=>Symbol).</p>
    <p>Be sure to use symbols that are valid on Yahoo Finance.  Symbols will automaticall be converted to upper-case.</p>
		
	<fieldset class="option">
		<legend>Stock Quote Mappings</legend>
		<textarea name="quotemappings" id="quotemappings" style="width: 98%; font-family: \"Courier New\", Courier, mono;" rows="15" cols="40">$replacements</textarea>
	</fieldset>
	<div class="submit"><input type="submit" name="Submit" value="Save changes &raquo;" /></div>
</form>
	</div>
END;
}

