add_filter('the_content', 'sbq_the_content');

function sbq_the_content($content) {
	$options = get_option('sbq');
	$sbmaps  = $options['sanebullquotes'];
	$search_strings = array_keys($sbmaps);
	$replace_strings = array_values($sbmaps);
	$search_strings = array_map('sb_search', $search_strings);
	$replace_strings = array_map('sb_replace', $replace_strings);
	$content = preg_replace($search_strings, $replace_strings, $content);
	return $content;
}

function sb_search($value){
//    return '/(\b)(' . $value . ')(\b)(?!([^\<\>]*\"\>|[^\<]*\<\/a[^\<\>]*\>))/i';
    return '/\(' . $value . '\)/';
}

function sb_replace($value) {
	if (is_feed()) {
		return '<a href="http://sanebull.com/m?symbol=' . $value . '">\\0</a>';
	}

	return '<a href="http://sanebull.com/m?symbol=' . $value . '" class="sb_quote" onmouseover="SBPlugin.showQuote(this,  \''.$value.'\' )" onclick="SBPlugin.expandQuote( \''.$value.'\' );return false;">\\0<span id="sb_ticker" class="' . $value . '" ></span></a>';
}
   
